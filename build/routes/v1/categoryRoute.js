import { Router } from "express";
import middlewareHandler from "../../src/middlewares/_middleware.js";
import control from "../../src/utils/control.js";
import sanitizeAndValidate from "../../src/utils/validate.js";
import categoryController from "../../src/controllers/categoryController.js";
import categoryValidation from "../../src/validations/categoryValidation.js";
const router = Router();
router.get("/", control(categoryController.index));
router.get("/:categoryId", middlewareHandler("auth"), sanitizeAndValidate(categoryValidation.selectedCategory), control(categoryController.selected));
router.post("/", middlewareHandler("auth"), sanitizeAndValidate(categoryValidation.postCategory), control(categoryController.create));
router.put("/:categoryId", middlewareHandler("auth"), sanitizeAndValidate(categoryValidation.putCategory), control(categoryController.update));
router.patch("/:categoryId", middlewareHandler("auth"), sanitizeAndValidate(categoryValidation.patchCategory), control(categoryController.update));
router.delete("/:categoryId", middlewareHandler("auth"), sanitizeAndValidate(categoryValidation.destroyCategory), control(categoryController.destroy));
export default router;
