import { Router } from "express";
import middlewareHandler from "../../src/middlewares/_middleware.js";
import control from "../../src/utils/control.js";
import sanitizeAndValidate from "../../src/utils/validate.js";
import toolController from "../../src/controllers/toolController.js";
import toolValidation from "../../src/validations/toolValidation.js";
const router = Router();
router.get("/", control(toolController.index));
router.get("/:toolId", middlewareHandler("auth"), sanitizeAndValidate(toolValidation.selectedTool), control(toolController.selected));
router.post("/", middlewareHandler("auth"), sanitizeAndValidate(toolValidation.postTool), control(toolController.create));
router.put("/:toolId", middlewareHandler("auth"), sanitizeAndValidate(toolValidation.putTool), control(toolController.update));
router.patch("/:toolId", middlewareHandler("auth"), sanitizeAndValidate(toolValidation.patchTool), control(toolController.update));
router.delete("/:toolId", middlewareHandler("auth"), sanitizeAndValidate(toolValidation.destroyTool), control(toolController.destroy));
export default router;
