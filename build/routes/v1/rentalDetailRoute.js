import { Router } from "express";
import middlewareHandler from "../../src/middlewares/_middleware.js";
import control from "../../src/utils/control.js";
import sanitizeAndValidate from "../../src/validations/_validate.js";
import rentalDetailController from "../../src/controllers/rentalDetailController.js";
import rentalDetailValidation from "../../src/validations/rentalDetailValidation.js";
const router = Router();
router.get("/", control(rentalDetailController.index));
router.get("/:detailId", middlewareHandler("auth"), sanitizeAndValidate(rentalDetailValidation.selectedRentalDetail), control(rentalDetailController.selected));
router.post("/", middlewareHandler("auth"), sanitizeAndValidate(rentalDetailValidation.createRentalDetail), control(rentalDetailController.create));
router.put("/:detailId", middlewareHandler("auth"), sanitizeAndValidate(rentalDetailValidation.updateRentalDetail), control(rentalDetailController.update));
router.patch("/:detailId", middlewareHandler("auth"), sanitizeAndValidate(rentalDetailValidation.updateRentalDetail), control(rentalDetailController.update));
router.delete("/:detailId", middlewareHandler("auth"), sanitizeAndValidate(rentalDetailValidation.destroyRentalDetail), control(rentalDetailController.destroy));
router.delete("/:detailId/force", middlewareHandler("auth"), sanitizeAndValidate(rentalDetailValidation.destroyRentalDetail), control(rentalDetailController.destroyNotRestoreToolsStock));
export default router;
